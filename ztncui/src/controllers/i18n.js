function normalize(lang) {
  return supported.includes(lang) ? lang : 'en';
}

exports.middleware = function(req, res, next) {
  const requested = req.query.lang || (req.cookies && req.cookies.lang);
  const lang = normalize(requested);
  res.locals.lang = lang;
  res.locals.i18nMessages = lang === 'zh-CN' ? zhCN : {};
  res.locals.langUrl = function(nextLang) {
    return '/language/' + encodeURIComponent(nextLang) + '?redirect=' + encodeURIComponent(req.originalUrl || '/');
  };
  next();
};

exports.setLanguage = function(req, res) {
  const lang = normalize(req.params.lang);
  res.cookie('lang', lang, { maxAge: 365 * 24 * 60 * 60 * 1000, httpOnly: true, sameSite: 'lax' });
  res.redirect(redirects.safeRedirectTarget(req.query.redirect, '/'));
};
